//* AND (&&)

true  && true  == true; // Unico caso en el que es verdadero.
true  && false == false;
false && true  == false;
false && false == false;

//* OR (||)

true  || true  == true;
true  || false == true;
false || true  == true;
false || false == false; // Unico caso en el que es falso.

//* NOT (!)

!false == true;
!true  == false;